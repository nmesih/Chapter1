package chapter01.exerxises;

public class E10 {
	/* 
	 * Assume a runner runs 14 kilometers in 45 minutes and 30 seconds. 
	 * Write a program that displays the average speed in miles per hour. 
	 * (Note that 1 mile is 1.6 kilometers.)
	 */
	public static void main(String[] args) {
		System.out.println("14 kilometers to miles is " + (14 / 1.6) + ".");
		System.out.println("45 minutes and 30 seconds to hour is " + (45.5 / 60) + ".");
		System.out.println("Runner run " + (8.75 / 0.7583333333333333) + " miles per hour.");
		
	}
}
