package chapter01.exerxises;

public class E8 {
	/*
	 *  Write a program that displays the area and perimeter of a circle that has a radius of 5.5 using the following formula:
		perimeter = 2 * radius * pi
		area = radius * radius * pi
		
		radius = 5.5
		pi = 3.14
	 */
	
	public static void main(String[] args ) {
		System.out.println("Perimeter = 2 * radius * pi" +
				"\nPerimeter = 2 * 5.5 * 3.14" +
				"\nPerimeter = " + (2 * 5.5 * 3.14));
		
		System.out.println("Area = radius * radius * pi" +
				"\nArea = 5.5 * 5.5 * 3.14" +
				"\nArea = " + (5.5 * 5.5 * 3.14));
		
	}

}
