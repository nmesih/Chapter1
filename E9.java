package chapter01.exerxises;

public class E7 {
	/*
	 * Write a program that displays the area and perimeter 
	 * of a rectangle with the width of 4.5 and height of 
	 * 7.9 using the following formula:
	 * 
       area = width * height
       perimeter = 2 * (width + height)
        */
	public static void main(String[] args ) {
		System.out.println("Area of a Rectangle = Width * Height\n" +
		"Area = " + (4.5 * 7.9));
		
		System.out.println("Perimeter of a Rectangle = 2 * (Width + Height)\n" +
				"Perimeter = " + (2 * (4.5 + 7.9)));
		
	}

}
